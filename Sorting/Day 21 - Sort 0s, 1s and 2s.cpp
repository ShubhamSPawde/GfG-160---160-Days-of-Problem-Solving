class Solution {
  public:
    void sort012(vector<int>& arr) {
        int cnt1 = 0, cnt2 = 0, cnt0 = 0;
        for(int i = 0; i<arr.size(); i++){
            if(arr[i] == 0){
                cnt0++;
            }else if(arr[i] == 1){
                cnt1++;
            }else{
                cnt2++;
            }
        }
        int k = 0;
        while(cnt0--){
            arr[k++] = 0;
        }
        while(cnt1--){
            arr[k++] = 1;
        }
        while(cnt2--){
            arr[k++] = 2;
        }
    }
};


